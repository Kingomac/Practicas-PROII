public class FechaFormatoException extends Exception {

    /**
     * Creates a new instance of <code>FechaFormatoException</code> without detail message.
     */
    public FechaFormatoException() {
    }


    /**
     * Constructs an instance of <code>FechaFormatoException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public FechaFormatoException(String msg) {
        super(msg);
    }
}
